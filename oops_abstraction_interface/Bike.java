package oops_abstraction_interface;

public class Bike implements Vehicle {
	
	public void start() {
		System.out.println("Start the Bike !!");
		
	}
	
	public void drive() {
		System.out.println("Drive the Bike...........");
		
	}
	
	public void stop() {
		System.out.println("Stop the Bike!!!!!!!!!!!");
	}
	

}
